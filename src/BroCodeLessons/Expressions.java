package BroCodeLessons;

public class Expressions {
    public static void main(String[] args){

        int x = 23;

        System.out.println("x: " + x);
        x++;
        System.out.println("x: " + x);
        --x;
        System.out.println("x: " + x--);
        System.out.println("x: " + x);
        x+=1;

        System.out.println("x/2 = " + x/2);

        System.out.println("x/2 = " + (float)x/2);
    }
}
