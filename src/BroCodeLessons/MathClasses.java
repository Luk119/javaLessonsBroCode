package BroCodeLessons;

public class MathClasses {
    public static void main(String[] args){

        float x = 3.14f;
        float y = 10f;

        System.out.println("abs(a): " + Math.abs(-10));//absolut value
        System.out.println("pow(a): " + Math.pow(y, 3));//power
        System.out.println("sqrt(a): " + Math.sqrt(y));
        System.out.println("round(a): " + Math.round(x));
        System.out.println("ceil(a): " + Math.ceil(x));
        System.out.println("floor(a): " + Math.floor(x));

    }
}
